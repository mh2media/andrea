<h2>What is Depression?</h2>

<p>Depression is a condition when a person feels sad, discouraged, hopeless or unmotivated.  It's not a sign of weakness. It's not a lack of character or strength. It is a serious illness that can be treated - and generally best treated with professional help.  Depression can also co-occur with anxiety.  In fact, it is not uncommon for a person with an anxiety disorder to also suffer from depression or vice versa.</p>

 

<h2>Am I just sad or a maybe just a little nervous?</h2>

<p>It's possible. But if you are sad more often than you are happy, if you can't face going to work another day or dealing with family or other life issues; or if you are so anxious that it is interferring with your ability to function with your normal routine, you may want to consider professional treatment.</p>

 

<h2>It's different for everyone.</h2>

<p>You don't necessarily have to be so depressed that you are confined to your bed or can't leave your house. In fact, much of the time, the symptoms of depression are more apt to show themselves in far less dramatic fashion. Arguments with your spouse or significant other over mundane issues. Feeling tired all the time. Muscle fatigue. Even physical symptoms such as backache or headache may be early indicators of depression.</p>

 

