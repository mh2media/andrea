    <h1>What is a Marriage and Family Therapist?</h1>

<p>Marriage and Family Therapists (MFTs) are relationship specialists who treat persons involved in interpersonal relationships.  They are trained to assess, diagnose and treat individuals, couples, families and groups to achieve more adequate, satisfying and productive marriage, family and social adjustment.  The practice also includes premarital counseling, child counseling, divorce or separation counseling and other relationship counseling. California MFTs are psychotherapists and healing arts practitioners licensed by the Board of Behavioral Sciences.</p>

 

 <h2>What are the Qulifications for MFTs?</h2>

 <p>MFTs earn their license through a rigorous education, traning and licensing process similar to other mental health professionals. Before obtaining the MFT license, Marriage and Family Therapists must first complete a masters or doctoral degree program.  They are then required to complete a minimum of 3000 hours of clinical supervised experience. After completion of their clinical training, MFTs are required to pass two competency-based tests before obtaining licensure. An emphasis of the MFTs training is diagnosis and treatment of psychopathology from a family system and relationship perspective.</p>

 

