<h2>What is the correct way to grieve and how long will the grieving last?</h2>
<p>There is no "right way" or  "wrong way" to grieve.  Each person experiences grief in his or her own way and at their own rate.  It is important to have a good support system immediately after the loss and especially for the several months thereafter.</p>

<h2>Are there other types of loss?</h2>
<p>There are many types of loss.  Not only do we experience loss from the death of a loved one, but we also experience loss through divorce, job loss, physical impairment, life changes and transitions, the launching of our children and many other ways.  Some losses can be more traumatic than others.</p>

<h2>What is the difference between loss and trauma?</h2>
<p>Although experiencing a traumatic event oftentimes involves loss, trauma is usually defined as having experienced a highly stressful and frightening experience or event which causes intense distress and a perceived sense of harm to the self.</p>

<h2>Trauma treatment</h2>

<p>Many people who have been exposed to a catastrophic event such as a car accident, natural disaster, rape or war-related incident experience both traumatic and post-traumatic stress reactions.  Most are able to survive and cope well.  A small percentage of people, however, will develop PTSD (Post Traumatic Stress Disorder)</p>

 
<h2>Treatment for PTSD</h2>

<p>There are a number of treatment options for PTSD? Some of the treatment options may include Cognitive-Behavioral therapy, Exposure Therapy, medication, anxiety managment training strategies such as relaxation and stress reduction and the technique of EMDR.</p>

 

